package examen_ejercicio_1;

/*1. Escribe un programa en el que se declaren variables usando los 8 tipos de dato básico que
hemos trabajado en clase. Cada una de las variables se debe inicializar. Por último, se debe
mostrar por pantalla el contenido de cada una de las variables.*/

public class Examen_ejercicio_1 {
    public static void main(String[] args) {
        
        //Declaro y inicializo variables
        byte a = 2;
        short b = 20;
        int c = 200;
        long d = 20000;
        char letra = 64;
        float e = 2;
        double f = 200.2;
        boolean boo = true;
        
        //Imprimo por pantalla
        System.out.println("el valor de byte es: "+a);
        System.out.println("el valor de short es: "+b);
        System.out.println("el valor de int es: "+c);
        System.out.println("el valor de long es: "+d);
        System.out.println("el valor de char es: "+letra);
        System.out.println("el valor de float es: "+e);
        System.out.println("el valor de double es: "+f);
        System.out.println("el valor de boolean es: "+boo);
 
    
    }
}
    

